public class UnaryOperator {

    public static void main(String[] args) {

        // operator yang ditempatkan di depan

        int a = 10;
        boolean hasil = true;

        System.out.println(a++);
        System.out.println(a--);
        System.out.println(-a);
        System.out.println(+a);

        System.out.println(!hasil);
    }
}
