public class OperasiPerbandingan {

    public static void main(String[] args) {

        // operasi memmbandingkan 2 data
        // menghasilkan true atau false


        int value1 = 100;
        int value2 = 100;


        System.out.println(value1 > value2);
        System.out.println(value1 < value2);
        System.out.println(value1 >= value2);
        System.out.println(value1 <= value2);
        System.out.println(value1 == value2);
        System.out.println(value1 != value2);



    }
}
