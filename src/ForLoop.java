public class ForLoop {

    // for adalah kata kunci melakukan perulangan

    public static void main(String[] args) {

        // for(init statement; kondisi; post statement){
        // block perulangan }

        var counter = 1;

        for (; counter <= 10;) {
            System.out.println("Perulangan" + counter);
            counter++;
        }
    }

}
